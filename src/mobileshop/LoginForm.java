    private void view(String sql) {

        try {
            ResultSet rs = db.exSelect(sql);
            boolean found = false;

            while (rs.next()) {
                found = true;
                productIDTextField.setText(rs.getString("productID"));
                productTypeTextFeild.setText(rs.getString("productType"));
                productNameTextField.setText(rs.getString("productName"));
                productColorTextField.setText(rs.getString("productColor"));
                productPriceTextField.setText(rs.getDouble("productPrice") + "");
                ProductQuantityTextField.setText(rs.getInt("productQuantity") + "");
            }
            if (found) {
                productIDTextField.setEditable(false);
                productNameTextField.setEditable(false);
                updateStuckButton.setEnabled(true);
                viewStuckButton.setEnabled(false);
                newPriceTextFeild.setEnabled(true);
                addQuantityTextFeild.setEnabled(true);
                productTypeTextFeild.setEnabled(true);
                productPriceTextField.setEnabled(true);
                productPriceTextField.setEditable(false);
                ProductQuantityTextField.setEnabled(true);
                ProductQuantityTextField.setEditable(false);
                newPriceTextFeild.setEditable(true);

                addQuantityTextFeild.setEditable(true);

            } else {
                JOptionPane.showMessageDialog(this, "البينات غير صحيحه");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

